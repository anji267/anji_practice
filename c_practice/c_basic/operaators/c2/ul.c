#include<stdio.h>
int main()
{
        char ch;
        printf("the upper case letters are: \n");
        for(ch=65;ch<=90;ch++)
        {
                printf("%c\n",ch);
        }
        printf("\n");
        printf("the lower case letters are: \n");
        for(ch=96;ch<=122;ch++)
        {
                printf("%c \n",ch);
        }
}
